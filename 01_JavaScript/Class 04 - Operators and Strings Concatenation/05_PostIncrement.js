let num = 1;

console.log("O valor atual da variável número é: ", num);
num++;
console.log("O valor pós-incrementado da variável número é: ", num);

/* 

No Operador de Pós-Incremento, a variável "num" é retornada com o valor original e só depois o seu valor atual é incrementado. Observe que adicionamos um novo console.log() para visualizar o valor atualizado da variável "num".

*/