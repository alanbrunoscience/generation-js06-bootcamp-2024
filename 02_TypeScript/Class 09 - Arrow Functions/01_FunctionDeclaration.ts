// Function Declaration

export function add(number1: number, number2: number): number {
    return number1 + number2;
}

/*

- Um ponto importante, que merece ser destacado e reforçado em relação as funções no TypeScript, é que não importa como a função é criada, uma função é um valor e este valor será armazenado em uma variável chamada 'add'. Neste exemplo, 'add' é o identificador (nome) da função.

*/