/* Number

- No TypeScript, todos os valores numéricos, como: int (inteiro), float (real) , decimal , hexadecimal , octal, entre outros, devem ser tipados como Number. Em outras palavras, no TypeScript número é número.

*/

let integerNumber: number = 10;
console.log("'integerNumber' variable value: ", integerNumber);

let floatNumber: number = 10.25;
console.log("\n'floatNumber' variable value: ", floatNumber);

let hexNumber: number = 0x01;
console.log("\n'hexNumber' variable value: ", hexNumber);